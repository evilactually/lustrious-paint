#include <windows.h>
#include <stdio.h>
#include <fcntl.h>
#include <io.h>
#include <iostream>
#include <fstream>

//-------------------------------------------------------------------------------
//-- Functions ------------------------------------------------------------------
//-------------------------------------------------------------------------------

//-------------------------------------------------------------------------------
// @ LsAttachConsole
//-------------------------------------------------------------------------------
// Create a console and redirect all standard IO
//-------------------------------------------------------------------------------
void LsOpenConsole() {
  AllocConsole();
  freopen("CONOUT$", "w", stdout);
  freopen("CONOUT$", "w", stderr);
  freopen("CONOUT$", "r", stdin);
}

//-------------------------------------------------------------------------------
// @ LsAttachConsole
//-------------------------------------------------------------------------------
// Free console and close file handles
//-------------------------------------------------------------------------------
void LsCloseConsole() {
  fclose(stdout);
  fclose(stderr);
  fclose(stdin);
  FreeConsole();
}