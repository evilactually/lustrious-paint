#pragma once

//-------------------------------------------------------------------------------
//-- Dependencies ---------------------------------------------------------------
//-------------------------------------------------------------------------------

#include <tuple>

//-------------------------------------------------------------------------------
//-- Structs --------------------------------------------------------------------
//-------------------------------------------------------------------------------

//-------------------------------------------------------------------------------
// @ LsDomain
//-------------------------------------------------------------------------------
// Describes extent of isosurface domain
//-------------------------------------------------------------------------------
struct LsDomain {
  LsDomain() : x1(0), y1(0), z1(0), x2(0), y2(0), z2(0) {};
  LsDomain(float x1, float y1, float z1, float x2, float y2, float z2): x1(x1), y1(y1), z1(z1), x2(x2), y2(y2), z2(z2) {};
  float x1;
  float y1;
  float z1;
  float x2;
  float y2;
  float z2;
};

//-------------------------------------------------------------------------------
//-- Typedefs -------------------------------------------------------------------
//-------------------------------------------------------------------------------

//-------------------------------------------------------------------------------
// @ LsBCCNode
//-------------------------------------------------------------------------------
// Specifies coordinates of a node
//-------------------------------------------------------------------------------
typedef std::tuple<int, int, int> LsBCCNode;

//-------------------------------------------------------------------------------
// @ LsBCCEdge
//-------------------------------------------------------------------------------
// Specifies coordinates of nodes defining an edge
//-------------------------------------------------------------------------------
typedef std::tuple<LsBCCNode, LsBCCNode> LsBCCEdge;

//-------------------------------------------------------------------------------
// @ LsBCCTetrahedron
//-------------------------------------------------------------------------------
// Specifies coordinates of nodes defining a tetrahedron
//-------------------------------------------------------------------------------
typedef std::tuple<LsBCCNode, LsBCCNode, LsBCCNode, LsBCCNode> LsBCCTetrahedron;

//-------------------------------------------------------------------------------
//-- Enums ----------------------------------------------------------------------
//-------------------------------------------------------------------------------

//-------------------------------------------------------------------------------
// @ LsBCCColor
//-------------------------------------------------------------------------------
// Specifies colors of either nodes or edges
//-------------------------------------------------------------------------------
enum class LsBCCColor
{
  eRed,
  eBlack
};

//-------------------------------------------------------------------------------
// @ LsBCCValue
//-------------------------------------------------------------------------------
// Specifies value of a cut function at a node
//-------------------------------------------------------------------------------
enum class LsBCCValue
{
  eUnassigned = 0,
  eNegative = 1,
  eZero = 2,
  ePositive = 3
};
